import torch
import torch.nn as nn
import torch.nn.functional as F


class ContentLoss(nn.Module):
    '''
    Content Loss, a function that represents a weighted version of the content distance for an individual layer
    We will add this content loss module directly after the convolution layers that are being used to compute the content distance.
    '''

    def __init__(self, target,):
        super(ContentLoss, self).__init__()
        self.target = target.detach()

    def forward(self, input):
        self.loss = F.mse_loss(input, self.target)
        return input


class StyleLoss(nn.Module):
    '''
    Style Loss, computes the style loss of each layer.
    '''

    def __init__(self, target_feature):
        super(StyleLoss, self).__init__()
        self.target = self.gram_matrix(target_feature).detach()

    def forward(self, input):
        G = self.gram_matrix(input)
        self.loss = F.mse_loss(G, self.target)
        return input

    def gram_matrix(self, input):
        a, b, c, d = input.size()  # a=batch size(=1)
        # b=number of feature maps
        # (c,d)=dimensions of a f. map (N=c*d)

        features = input.view(a * b, c * d)  # resise F_XL into \hat F_XL

        G = torch.mm(features, features.t())

        return G.div(a * b * c * d) # normalization
